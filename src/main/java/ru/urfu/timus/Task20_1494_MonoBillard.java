package ru.urfu.timus;

import java.io.BufferedReader;
import java.io.InputStreamReader;

public class Task20_1494_MonoBillard {
    public static void main(String[] args) throws Exception {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        int N = Integer.parseInt(br.readLine().trim());
        int[] out = new int[N];
        for (int i = 0; i < N; i++) {
            out[i] = Integer.parseInt(br.readLine().trim());
        }

        int[] st = new int[N];
        int top = -1, next = 1;
        boolean ok = true;
        for (int want : out) {
            while ((top < 0 || st[top] != want) && next <= N) {
                st[++top] = next++;
            }
            if (top >= 0 && st[top] == want) {
                top--;
            } else {
                ok = false;
                break;
            }
        }

        System.out.println(ok ? "Not a proof" : "Cheater");
    }
}

